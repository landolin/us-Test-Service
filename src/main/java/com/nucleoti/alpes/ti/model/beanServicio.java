package com.nucleoti.alpes.ti.model;

public class beanServicio {
	private String idServicio;
	private String stadoServicio;
	private String idCliente;
	private String idConductor;
	private String calificacion;
	private String fechaServicio;
	private String horaServicio;
	public String getIdServicio() {
		return idServicio;
	}
	public void setIdServicio(String idServicio) {
		this.idServicio = idServicio;
	}
	public String getStadoServicio() {
		return stadoServicio;
	}
	public void setStadoServicio(String stadoServicio) {
		this.stadoServicio = stadoServicio;
	}
	public String getIdCliente() {
		return idCliente;
	}
	public void setIdCliente(String idCliente) {
		this.idCliente = idCliente;
	}
	public String getIdConductor() {
		return idConductor;
	}
	public void setIdConductor(String idConductor) {
		this.idConductor = idConductor;
	}
	public String getCalificacion() {
		return calificacion;
	}
	public void setCalificacion(String calificacion) {
		this.calificacion = calificacion;
	}
	public String getFechaServicio() {
		return fechaServicio;
	}
	public void setFechaServicio(String fechaServicio) {
		this.fechaServicio = fechaServicio;
	}
	public String getHoraServicio() {
		return horaServicio;
	}
	public void setHoraServicio(String horaServicio) {
		this.horaServicio = horaServicio;
	}
	
	
}
